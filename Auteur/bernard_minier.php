				<h2>Biographie</h2>
				<p>Bernard Minier grandit à Montréjeau au pied des Pyrénées, puis fait des études à Tarbes et à Toulouse avant de séjourner un an en Espagne. Il vit aujourd’hui en Île-de-France.<br><br>Il fait d'abord carrière dans l'administration des douanes comme contrôleur principal, tout en participant à des concours de nouvelles avant de franchir le pas et d'envoyer un manuscrit de roman à des éditeurs.<br><br>Il publie son premier roman, Glacé, en 2011. Salué par la presse et Prix du meilleur roman francophone au Festival Polar de Cognac, Glacé a très vite connu un large succès public et a été traduit ou est en cours de traduction dans 22 langues, dont l’anglais. Il rencontre le même succès dans plusieurs pays européens. Glacé a été adapté en série télévisée par Gaumont Télévision et M6, diffusée sur M6 en 2017 et disponible sur Netflix depuis octobre 2017. En mai 2019, le Sunday Times le classe dans son Top 100 des meilleurs romans policiers depuis 1945.<br><br>Glacé a pour héros le commandant Martin Servaz, un policier de Toulouse profondément humain et lettré, confronté à une série de crimes aussi épouvantables qu’incompréhensibles dans les Pyrénées au cœur de l’hiver.<br><br>Le Cercle (2012), N’éteins pas la lumière (2014), Nuit (2017), Sœurs (2018) et La Vallée (2020) renouent avec le même personnage..<br><br>Il a également publié 2 unitaires : Une putain d’histoire (2015, prix du meilleur roman francophone du Festival Polar de Cognac), dont l’intrigue ancrée aux Etats-Unis évoque les dérives d'Internet et les menaces sur les libertés individuelles, et M, le bord de l'abîme (2019), dont l'histoire se déroule à Hong-Kong et aborde la puissance de la technologie et de l'Intelligence artificielle.<br><br>Ses livres, traduits en 22 langues, sont tous publiés aux Éditions XO et repris en format poche chez Pocket.<br><br>Il fait aussi partie du collectif d'artistes la Ligue de l'Imaginaire.</p>
